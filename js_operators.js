//addition (+)
var a = 5;
var b = 10;
var c = a + b;
console.log(c);

//subtraction(-)
var a = 5;
var b = 1;
var c = a - b;
console.log(c);

//multiplication(*)
var a = 2;
var b = 3;
var c = a * b;
console.log(c);

//exponentiation(**) or (power)
var a = 2;
var c = a ** 3;
console.log(c);

//Division(/)
var a = 10;
var b = 2;
var c = a / b;
console.log(c);

//Modulus(%)
var a = 10;
var b = 2;
var c = a % b;
console.log(c);

//increment(++)
var a = 5;
console.log(a++); //this gives 5 which is post increment
//a++ --> a = a + 1; OR a += 1

var a = 5;
console.log(++a); //this gives 5 which is pre increment

//decrement(--)
var a = 5;
console.log(a--); //this gives 5 which is post decrement
//a-- --> a = a - 1; OR a -= 1

var a = 5;
console.log(--a); //this gives 4 which is pre decrement

//some other operators
